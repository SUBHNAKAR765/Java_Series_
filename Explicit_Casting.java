public class Explicit_Casting {
    public static void main(String[] args) {
        int p = 100;
        float fp = p+(float)10; // here integer is converted into float .
        System.out.println(fp);


        int q = 100;
        int  fq = q +(int )18.00;  // here double is converted into integer .
        System.out.println(fq);

    }
}
